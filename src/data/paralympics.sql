BEGIN TRANSACTION;
PRAGMA foreign_keys = ON;
CREATE TABLE IF NOT EXISTS "event" (
	"event_id"	INTEGER NOT NULL,
	"type"	TEXT NOT NULL,
	"year"	INTEGER NOT NULL,
	"start"	TEXT,
	"end"	TEXT,
	"duration"	INTEGER,
	"countries"	INTEGER,
	"events"	INTEGER,
	"sports"	INTEGER,
	"highlights"	TEXT,
	"url"	TEXT,
	PRIMARY KEY("event_id")
);
CREATE TABLE IF NOT EXISTS "country" (
	"code"	TEXT NOT NULL,
	"name"	TEXT NOT NULL,
	"region"	TEXT,
	"sub_region"	TEXT,
	"member_type"	TEXT,
	"notes"	TEXT,
	PRIMARY KEY("code")
);
CREATE TABLE IF NOT EXISTS "disability" (
	"disability_id"	INTEGER NOT NULL,
	"category"	TEXT NOT NULL,
	PRIMARY KEY("disability_id")
);
CREATE TABLE IF NOT EXISTS "quiz" (
	"quiz_id"	INTEGER NOT NULL,
	"quiz_name"	TEXT NOT NULL,
	"close_date"	TEXT,
	PRIMARY KEY("quiz_id")
);
CREATE TABLE IF NOT EXISTS "disability_event" (
	"event_id"	INTEGER NOT NULL,
	"disability_id"	INTEGER NOT NULL,
	PRIMARY KEY("event_id","disability_id"),
	FOREIGN KEY("event_id") REFERENCES "event"("event_id"),
	FOREIGN KEY("disability_id") REFERENCES "disability"("disability_id")
);
CREATE TABLE IF NOT EXISTS "host" (
	"host_id"	INTEGER NOT NULL,
	"country_code"	TEXT,
	"host"	TEXT NOT NULL,
	PRIMARY KEY("host_id"),
	FOREIGN KEY("country_code") REFERENCES "country"("code")
);
CREATE TABLE IF NOT EXISTS "participants" (
	"participant_id"	INTEGER NOT NULL,
	"event_id"	INTEGER,
	"participants_m"	INTEGER,
	"participants_f"	INTEGER,
	"participants"	INTEGER,
	PRIMARY KEY("participant_id"),
	FOREIGN KEY("event_id") REFERENCES "event"("event_id")
);
CREATE TABLE IF NOT EXISTS "medal_result" (
	"result_id"	INTEGER NOT NULL,
	"event_id"	INTEGER,
	"country_code"	TEXT,
	"rank"	INTEGER,
	"gold"	INTEGER,
	"silver"	INTEGER,
	"bronze"	INTEGER,
	"total"	INTEGER,
	PRIMARY KEY("result_id"),
	FOREIGN KEY("country_code") REFERENCES "country"("code"),
	FOREIGN KEY("event_id") REFERENCES "event"("event_id")
);
CREATE TABLE IF NOT EXISTS "question" (
	"question_id"	INTEGER NOT NULL,
	"question"	TEXT NOT NULL,
	"event_id"	INTEGER,
	PRIMARY KEY("question_id"),
	FOREIGN KEY("event_id") REFERENCES "event"("event_id")
);
CREATE TABLE IF NOT EXISTS "student_response" (
	"response_id"	INTEGER NOT NULL,
	"student_email"	TEXT NOT NULL,
	"score"	INTEGER,
	"quiz_id"	INTEGER,
	PRIMARY KEY("response_id"),
	FOREIGN KEY("quiz_id") REFERENCES "quiz"("quiz_id")
);
CREATE TABLE IF NOT EXISTS "host_event" (
	"host_id"	INTEGER NOT NULL,
	"event_id"	INTEGER NOT NULL,
	PRIMARY KEY("host_id","event_id"),
	FOREIGN KEY("host_id") REFERENCES "host"("host_id") ON DELETE NO ACTION ON UPDATE CASCADE,
	FOREIGN KEY("event_id") REFERENCES "event"("event_id") ON DELETE NO ACTION ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS "quiz_question" (
	"quiz_id"	INTEGER NOT NULL,
	"question_id"	INTEGER NOT NULL,
	PRIMARY KEY("quiz_id","question_id"),
	FOREIGN KEY("quiz_id") REFERENCES "quiz"("quiz_id") ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY("question_id") REFERENCES "question"("question_id") ON DELETE CASCADE ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS "answer_choice" (
	"ac_id"	INTEGER NOT NULL,
	"question_id"	INTEGER,
	"choice_text"	TEXT,
	"choice_value"	INTEGER,
	"is_correct"	INTEGER,
	PRIMARY KEY("ac_id"),
	FOREIGN KEY("question_id") REFERENCES "question"("question_id")
);
